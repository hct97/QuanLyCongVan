package com.trunghc.model;

public class CoQuan {
    String idCoQuan;
    String tenCoQuan;

    public CoQuan() {
        this.idCoQuan = idCoQuan;
        this.tenCoQuan = tenCoQuan;
    }

    public String getIdCoQuan() {
        return idCoQuan;
    }

    public void setIdCoQuan(String idCoQuan) {
        this.idCoQuan = idCoQuan;
    }

    public String getTenCoQuan() {
        return tenCoQuan;
    }

    public void setTenCoQuan(String tenCoQuan) {
        this.tenCoQuan = tenCoQuan;
    }
}
